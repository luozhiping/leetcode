# 200. 岛屿数量
# https://leetcode-cn.com/problems/number-of-islands/

class Solution(object):
    def numIslands(self, grid):
        """
        :type grid: List[List[str]]
        :rtype: int
        """
        if not grid:
            return 0
        result = 0
        start = False

        dirs = [[0, 1], [0, -1], [1, 0], [-1, 0]]
        for i in range(len(grid)):
            for j in range(len(grid[0])):
                if grid[i][j] == "1":
                    start = True
                    result += 1
                    seq = [[i, j]]
                    grid[i][j] = "-1"
                    while seq:
                        cur = seq.pop(0)
                        for dir in dirs:
                            c = [cur[0] + dir[0], cur[1] + dir[1]]
                            if c[0] < 0 or c[0] == len(grid) or c[1] < 0 or c[1] == len(grid[0]):
                                continue
                            if grid[c[0]][c[1]] != "1":
                                continue
                            seq.append(c)
                            grid[c[0]][c[1]] = "-1"
        return result

